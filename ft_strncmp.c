int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				resp;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && n - 1 > i)
	{
		i++;
	}
	resp = s1[i] - s2[i];
	return (resp);
}
