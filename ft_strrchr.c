char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	x;

	i = 0;
	x = -1;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			x = i;
		i++;
	}
	if (x != -1)
		return (&s[x]);
	return (0);
}
